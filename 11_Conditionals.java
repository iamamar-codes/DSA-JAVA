class Conditionals {
    /*1. IF - Statement
    2. IF-ELSE Statement
    3. IF-ELSE-IF Leadder
    4. NESTED IF-ELSE
    5. TERNARY OPERATOR
    6. SWITCH Statement */
    public static void main(String[] args) {
        //1. IF - Statement
        int age = 10;

        if(age >= 18){
            System.out.println("You are eligible for Job");
        }

        //2. IF-ELSE Statement
        if(age >= 18){
            System.out.println("Your Eligible for Vote");
        }
        else{
            System.out.println("Your Not Eligible for Vote");
        }

    }
}
